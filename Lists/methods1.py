# append, insert and extend

exampleapend = ["string", True, 2.65, "stringagain"]
exampleapend.append("hello")
print(exampleapend)

exampleextend = ["string", True, 2.65, "stringagain"]
exampleextend.extend([1])
print(exampleextend)

exampleinsert = ["string", True, 2.65, "stringagain"]
exampleinsert.insert(1, "hello")
print(exampleinsert)

