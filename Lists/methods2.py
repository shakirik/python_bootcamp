# clear, pop and remove methods

exampleclear = ["string", True, 2.65, "stringagain"]
exampleclear.clear()
print(exampleclear)

examplepop = ["string", True, 2.65, "stringagain"]
examplepop.pop(1)
print(examplepop)

exampleremove = ["string", True, 2.65, "stringagain"]
exampleremove.remove("string")
print(exampleremove)